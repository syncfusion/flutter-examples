                                              activeColor:
                                                  model.backgroundColor,
                                              switchValue: toggleVisibility,
                                              valueChanged: (dynamic value) {
                                                setState(() {
                                                  toggleVisibility = value;
                                                });
                                              },
                                            ),
                                          ),
                                        ],
                                      ),
                                    ),
                                  ],
                                ),
                              )
                            ],
                          ),
                        ),
                      )),
                ))));
  }

  void onPositionTypeChange(String item, SampleListModel model) {
    setState(() {
      _selectedPosition = item;
      if (_selectedPosition == 'auto') {
        _position = LegendPosition.auto;
      }
      if (_selectedPosition == 'bottom') {
        _position = LegendPosition.bottom;
      }
      if (_selectedPosition == 'right') {
        _position = LegendPosition.right;
      }
      if (_selectedPosition == 'left') {
        _position = LegendPosition.left;
      }
      if (_selectedPosition == 'top') {
        _position = LegendPosition.top;
      }
      // ignore: invalid_use_of_protected_member
      model.notifyListeners();
    });
  }

  void onModeTypeChange(String item, SampleListModel model) {
    setState(() {
      _selectedMode = item;
      if (_selectedMode == 'wrap') {
        _overflowMode = LegendItemOverflowMode.wrap;
      }
      if (_selectedMode == 'scroll') {
        _overflowMode = LegendItemOverflowMode.scroll;
      }
      if (_selectedMode == 'none') {
        _overflowMode = LegendItemOverflowMode.none;
      }
      // ignore: invalid_use_of_protected_member
      model.notifyListeners();
    });
  }
}

class BackPanel extends StatefulWidget {
  //ignore: prefer_const_constructors_in_immutables
  BackPanel(this.sample);
  final SubItemList sample;
  @override
  _BackPanelState createState() => _BackPanelState(sample);
}

class _BackPanelState extends State<BackPanel> {
  _BackPanelState(this.sample);
  final SubItemList sample;
  final GlobalKey _globalKey = GlobalKey();
  
  @override
  void initState() {
    WidgetsBinding.instance.addPostFrameCallback(_afterLayout);
    super.initState();
  }

  void _afterLayout(dynamic _) {
    _getSizesAndPosition();
  }

  void _getSizesAndPosition() {
    final RenderBox renderBoxRed = _globalKey.currentContext.findRenderObject();
    final Size size = renderBoxRed.size;
    final Offset position = renderBoxRed.localToGlobal(Offset.zero);
    const double appbarHeight = 60;
    BackdropState.frontPanelHeight =
        position.dy + (size.height - appbarHeight) + 20;
  }

  @override
  Widget build(BuildContext context) {
    return ScopedModelDescendant<SampleListModel>(
      rebuildOnChange: true,
      builder: (BuildContext context, _, SampleListModel model) {
        return Container(
          color: model.backgroundColor,
          child: Padding(
            padding: const EdgeInsets.fromLTRB(10, 0, 0, 0),
            child: Column(
              mainAxisAlignment: MainAxisAlignment.start,
              crossAxisAlignment: CrossAxisAlignment.start,
              children: <Widget>[
                Text(
                  sample.title,
                  textAlign: TextAlign.left,
                  style: const TextStyle(
                      fontWeight: FontWeight.bold,
                      fontSize: 28.0,
                      color: Colors.white,
                      letterSpacing: 0.53),
                ),
                Padding(
                  key: _globalKey,
                  padding: const EdgeInsets.fromLTRB(0, 10, 0, 0),
                  child: Text(
                    sample.description,
                    style: const TextStyle(
                        fontWeight: FontWeight.normal,
                        fontSize: 15.0,
                        color: Colors.white,
                        letterSpacing: 0.3,
                        height: 1.5),
                  ),
                ),
              ],
            ),
          ),
        );
      },
    );
  }
}

List<PieSeries<_PieData, String>> getPieSeries(bool isTileView) {
  final List<_PieData> pieData = <_PieData>[
    _PieData('Tution Fees', 21),
    _PieData('Entertainment', 21),
    _PieData('Private Gifts', 8),
    _PieData('Local Revenue', 21),
    _PieData('Federal Revenue', 16),
    _PieData('Others', 8)
  ];
  return <PieSeries<_PieData, String>>[
    PieSeries<_PieData, String>(
        dataSource: pieData,
        xValueMapper: (_PieData data, _) => data.xData,
        yValueMapper: (_PieData data, _) => data.yData,
        startAngle: 90,
        endAngle: 90,
        dataLabelSettings: DataLabelSettings(isVisible: true)),
  ];
}

class _PieData {
  _PieData(this.xData, this.yData);
  final String xData;
  final num yData;
}

SfCircularChart getLegendOptionsChart(bool isTileView,
    [LegendPosition _position,
    LegendItemOverflowMode _overflowMode,
    dynamic toggleVisibility]) {
  return SfCircularChart(
    title: ChartTitle(text: isTileView ? '' : 'Expenses by category'),
    legend: Legend(
        isVisible: true,
        position: _position,
        overflowMode: _overflowMode,
        toggleSeriesVisibility: toggleVisibility),
    series: getPieSeries(isTileView),
    tooltipBehavior: TooltipBehavior(enable: true),
  );
}
